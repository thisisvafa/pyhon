# Operators: + - * /
# **

print(3 ** 3) # means: 3 power of 2
print(3 ** 6)
print(3 ** 9)
print(369 ** 0.9)

print('------------------------------')

# % return what remains from division
print(68 / 6)
print(68 % 6)
print(78 % 2)

print('------------------------------')

# // return integer part of a division
print((68 - (68 % 6)) / 6)
print(68 // 6)

